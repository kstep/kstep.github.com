  /**
   * The identity object is passed with all requests, to identify the request's user and session.

    sessionid - would be fetched from cookies/local storage if exists and valid (timeout of 1.5 hours since last activity), or be generated client-side otherwise with a timeout
    userId - would be fetched from cookies/local storage if exists, or be generated client-side otherwise
    customLoginId - would be fetched from cookies/local storage if exists. we would take it from the custom login process of the site. this field is for cross-platform&cross-sites identification.

    The "extendSession()" method extends the expiretion timeout of the session, and should be called after each activity (event tracking for example).
   */
  function Identity(site, siteVersion, siteType, customLoginId, sessionExpiresSec) {
    if (!site) throw new Error("argument 'site' is required")
    if (!sessionExpiresSec) throw new Error("argument 'sessionExpiresSec' is required")

    var self = this;

    // Private fields
    var isNewSession = false;

    self.site = site;
    self.siteVersion = siteVersion;
    self.siteType = siteType;
    self.timezone = currentTimeZoneOffset();

    self.generateSession = function() {
      self.sessionId = generateUUID();
      CacheMgr.set("crm_sessionId", self.sessionId, sessionExpiresSec);
      isNewSession = true;
    }

    self.killSession = function() {
      self.sessionId = null;
      CacheMgr.set("crm_sessionId", undefined);
      isNewSession = true;
    };

    // Get user id from cache
    self.userId = CacheMgr.get("crm_userId");
    // If not exists, generate and save to cache
    if (!self.userId) {
      self.userId = generateUUID();
      CacheMgr.set("crm_userId", self.userId);
    }

    // Use custom login id if provided, otherwise try to get from cache
    self.customLoginId = customLoginId || CacheMgr.get("crm_customLoginId");
    // Make sure stored in cache
    CacheMgr.set("crm_customLoginId", self.customLoginId);

    // Get session id from cache
    self.sessionId = CacheMgr.get("crm_sessionId");

    self.campaign = getCampaignData();

    // If not exists or campaign fields changed, generate and save to cache with timeout.
    // Note, we don't need to do campaign data check in getCampaignData() (it would put bad
    // unexpected side effect into the function, blurring business logic even further).
    // If old campaign data are modified at the same moment as current campaign data,
    // it means old campaign data was *just set* from current campaign data,
    // that is, campaign data has been changed.
    // Also note, campaign data are retrieved on page load only, so no need to recheck
    // this condition until next page load.
    if (self.campaign.old.ts === self.campaign.now.ts || !self.sessionId) {
      self.generateSession();
    }

    //* Public methods *//

    // If session is not expired, it should reside in cookie,
    // otherwise, if session was expired by timeout, Cache.get()
    // would return undefined.
    self.isSessionAlive = function () {
      return self.sessionId && CacheMgr.get("crm_sessionId");
    };

    // Extends the session timeout
    self.extendSession = function() {
      // If session is already dead, start a new one
      if (!identity.isSessionAlive()) {
        startSession();
      }

      CacheMgr.set("crm_sessionId", self.sessionId, sessionExpiresSec);
    }

    // Set custom login id
    self.setCustomLoginId = function(id) {
      self.customLoginId = id;
      CacheMgr.set("crm_customLoginId", id);
    }

    self.isNewSession = function() { return isNewSession }

    //* Private methods *//
    function currentTimeZoneOffset() {
      var now = new Date(),
      offset = -now.getTimezoneOffset(),
      pad = function (d) { return d < 10? "0" + d: "" + d; },
      hours = pad(Math.abs(Math.floor(offset / 60))),
      minutes = pad(Math.abs(offset % 60)),
      sign = offset < 0? "-": "+";
      return sign + hours + ":" + minutes;
    }

    function getCampaignData() {
      var referrer = win.referrer || doc.referrer,
          referrerParams = extractParamsFromUrl(referrer),
          pageParams = extractParamsFromUrl(win.location.search),
          campaignData = CacheMgr.get("crm_campaign"),
          timestamp = (new Date()).getTime(),
          cachedCampaign = {};

      if (campaignData) {
        try {
          cachedCampaign = JSON.parse(campaignData);
        } catch (e) {
          if (e instanceof SyntaxError) { // bad json
            console.log("ignoring crm campaign data from storage");
          } else {
            throw e;
          }
        }
      }

      var campaign = {
        group: pageParams.utm_adgroup_id || undefined,
        adid: pageParams.utm_ad_id || undefined,
        source: pageParams.utm_source || undefined,
        name: pageParams.utm_campaign || undefined,
        campaignid: pageParams.utm_campaign_id || undefined,
        content: pageParams.utm_content || undefined,
        term: pageParams.utm_term || undefined,
        medium: pageParams.utm_medium || undefined,
        clickid: pageParams.gclid || undefined,
        ts: timestamp
      };

      // if some campaign field is set to a new value, we override all campaign data
      for (var name in campaign) {
        if (name !== "ts" && campaign[name] !== undefined) {
          cachedCampaign = campaign;
          break;
        }
      }

      CacheMgr.set("crm_campaign", JSON.stringify(cachedCampaign));
      return { now: campaign, old: cachedCampaign };
    }
  }

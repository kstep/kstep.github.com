  // CacheMgr is a wrapper for using cookies, with fallback to localstorage {{{
  function CacheMgr() {
    var self = this;

    // Prefer cookies over localStorage, because localStorage is not cross-protocol. meaning that "http" pages would have different userid than "https" pages
    if (nav.cookieEnabled) {
      self.set = saveToCookie;
      self.get = loadFromCookie;
    } else {
      self.set = saveToLocalStorage;
      self.get = loadFromLocalStorage;
    }

    // Internal helper functions
    function saveToCookie(name, value, expireSec) {
      if (value === undefined) {
        expireSec = -999999999;
        value = "";
      }

      expireSec = expireSec || 9999999999;

      var date = new Date();
      date.setTime(date.getTime()+(expireSec*1000));
      var expires = "; expires="+date.toGMTString();

      doc.cookie = encodeURIComponent(name)+"="+encodeURIComponent(value)+expires+"; path=/";
    }

    function loadFromCookie(name) {
      var cookies = doc.cookie.split("; ");
      name = encodeURIComponent(name);

      for (var i = 0, l = cookies.length; i < l; i++) {
        var pair = cookies[i].split("=", 2);
        if (pair[0] == name) {
          return decodeURIComponent(pair[1]);
        }
      }
    }

    function saveToLocalStorage(name, value, expireSec) {
      if (value === undefined || expireSec < 0) {
        localStorage.removeItem(name);
        localStorage.removeItem("_ts_" + name);
        return;
      }

      expireSec = expireSec || 9999999999;

      localStorage.setItem(name, value);
      localStorage.setItem("_ts_" + name, (new Date().getTime()) + (expireSec * 1000));
    }

    function loadFromLocalStorage(name) {
      var value = localStorage.getItem(name) || undefined;
      if (value !== undefined) {

        var ts = parseInt(localStorage.getItem("_ts_" + name), 10) || 0;
        if (0 < ts && ts < (new Date().getTime())) {
          localStorage.removeItem(name);
          localStorage.removeItem("_ts_" + name);
          return;
        }

        return value;
      }
    }
  }

  // Server is the proxy to the backend, provides simple method to send the requests {{{
  function Server(baseUrl, sendMode) {
    var self = this;

    self.baseUrl = baseUrl;

    var XHR, sendRequest = realSend;

    var requestsQueue = [],
        errorsThreshold = 1,
        maxQueueLength = 10,
        queueTimeoutId = null,
        queueProcessTimeout = 500;

    if (win.XMLHttpRequest && "withCredentials" in new win.XMLHttpRequest()) {
      XHR = win.XMLHttpRequest;
    } else if (win.XDomainRequest) {
      XHR = win.XDomainRequest;
    }

    sendMode = sendMode || "pixel";
    switch (sendMode) {
      case "auto":
      case "ajax":
          if (!XHR) {
            sendRequest = realSendJsonp;
          }
          break;
      case "xhr":
          XHR = win.XMLHttpRequest;
          break;
      case "xdr":
          XHR = win.XDomainRequest;
          break;
      case "jsonp":
          sendRequest = realSendJsonp;
          break;
      case "img":
      case "pixel":
          sendRequest = realSendImage;
          break;
    }

    self.send = function (object, path, callback) {
      if (errorsThreshold < 1) {
        if (callback) callback(false, { status: 0 });
        return;
      }
      sendRequest(formatMessage(object), (path || "") + getPathByObject(object), callback);
    };

    var getLocalTime = function (siteName) {
      var now = new Date(),
          midnight = new Date();

      midnight.setDate(midnight.getDate() + 1);
      midnight.setHours(0); midnight.setMinutes(0);
      midnight.setSeconds(0); midnight.setMilliseconds(0);

      return {
        site: siteName,
        timezone: 'Local',
        ts: now.getTime(),
        midnightMillis: midnight.getTime() - now.getTime()
      };
    };

    self.getSiteTime = function (siteName, callback) {
      if (!XHR) {
        callback(getLocalTime());
        return;
      }

      var request = new XHR();
      request.open("GET", self.baseUrl + "/sitetime/" + siteName, true);

      request.onabort = request.ontimeout = request.onerror = function (ev) {
        callback(getLocalTime());
      };

      request.onload = function (ev) {
        try {
          if (request.status == 200) {
            callback(JSON.parse(request.responseText));
          } else {
            callback(getLocalTime());
          }
        } catch (e) {
          callback(getLocalTime());
        }
      };

      try {
        request.send();
      } catch (e) {
        callback(getLocalTime());
      }
    };

    // Enqueue request to be sent asynchronously
    self.enqueue = function (object, path, callback) {
      if (errorsThreshold < 1) {
        if (callback) callback(false, { status: 0 });
        return;
      }
      // add event to queue, but make sure queue is not too long
      requestsQueue.push([formatMessage(object), (path || "") + getPathByObject(object), callback]);

      persistRequestsQueue();

      if (requestsQueue.length > maxQueueLength) {
        self.runQueue();
      }

      // run queue in some timeout
      if (!queueTimeoutId) {
        queueTimeoutId = win.setTimeout(function () {
          self.runQueue();
          queueTimeoutId = null;
        }, queueProcessTimeout);
      }
    };

    self.runQueue = function () {
      var request;
      while (requestsQueue.length > 0) {
        request = requestsQueue.pop();
        sendRequest.apply(null, request);
      }

      persistRequestsQueue();
    };

    try {
      requestsQueue = JSON.parse(CacheMgr.get("crm_RQ") || "[]") || [];
    } catch (e) {
      console.warn("Error loading requests queue", e);
    }

    if (requestsQueue.length > 0) {
      self.runQueue();
    }

    function persistRequestsQueue() {
      CacheMgr.set("crm_RQ", JSON.stringify(requestsQueue));
    }

    function realSend(data, path, callback) {
      var request = new XHR();
      request.open("POST", self.baseUrl + "/data_collector" + path, true);
      request.setRequestHeader && request.setRequestHeader("Content-Type", "application/json; charset=utf-8");

      request.onabort = request.ontimeout = request.onerror = function (ev) {
        console.warn("Error sending track event", data, ev);
        errorsThreshold--;
        if (callback) callback(false, ev, request);
      };

      request.onload = function (ev) {
        if (request.status != 200) {
          console.warn("Track event rejected", data, ev);
        }
        if (callback) callback(true, ev, request);
      };

      request.send(data);
    }

    function realSendImage(data, path, callback) {
      var src = self.baseUrl + "/data_collector" + path + "?callback=pixel&data=" + encodeURIComponent(data);
      var img = new Image();
      img.src = src;
      if (callback) callback(true, { status: 200, responseText: "OK" });
    }

    function realSendJsonp(data, path, callback) {
      var queryid = generateUUID(),
          head = doc.getElementsByTagName("head")[0];

      win._crm._jsonpCallbacks[queryid] = [
        win.setTimeout(function () {
          head.removeChild(script);
          delete win._crm._jsonpCallbacks[queryid];
          errorsThreshold--;
          if (callback) {
            callback(false, { status: 0 });
          }
        }, 5000),
        function (reply) {
          head.removeChild(script);
          win.clearTimeout(win._crm._jsonpCallbacks[queryid][0]);
          delete win._crm._jsonpCallbacks[queryid];
          if (callback) {
            callback(true, { status: 200, responseText: reply });
          }
        }];
      var script = doc.createElement('script');
      script.async = true;
      script.src = self.baseUrl + "/data_collector" + path + "?callback=" + encodeURIComponent("_crm._jsonpCallbacks['" + queryid + "'][1]") + "&data=" + encodeURIComponent(data);
      head.appendChild(script);
    }

    function getPathByObject(object) {
      return "/report";
      //return object.getPath ? object.getPath() : "";
    }

    function formatMessage(object) {
      return object.toMessage? object.toMessage(): (
          typeof object === "string"? object:
          JSON.stringify(object));
    }
  }
